def _build_keyword_table(keywords: list[dict]) -> str:
    """키워드 점수 데이터를 프롬프트용 테이블로 변환한다."""
    if not keywords:
        return "(키워드 데이터 없음)"

    lines = ["키워드 | 검색량 | 경쟁도 | 점수"]
    for kw in keywords[:10]:
        lines.append(
            f"{kw['keyword']} | {kw['search_volume']:,} | "
            f"{kw['competition']} | {kw['score']:,.0f}"
        )
    return "\n".join(lines)


